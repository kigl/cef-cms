<p class="text-justive">
Коллекция включает лаконичные силуэты с акцентом на детали и мягкие объемы со сложными фактурными изгибами, офисные модели с элементами мужского стиля  и легкие романтичные в стиле casual. Уютные полупальто, вариации на тему графичных pied de poule в платьях и жакетах, гладкая костюмная ткань, принтованый фактурный трикотаж, струящиеся блузы из шифона и вискозы; полоска, клетка, жаккардовые узоры в самых смелых сочетаниях. Многоцветные вышивки, печатный принт с имитацией гравюры, нежные вязаные детали привлекают внимание  и предают WOW – эффект каждому образу коллекции.
</p>
<p class="text-justive">
Цветовая палитра достаточно разнообразна. Она олицетворяет единство, уверенность и сложность. Серый и его оттенки, воздушный синий, пудровые цвета – размеренные повседневные, цвета гончарной глины, насыщенный красный, с чувственными теплыми нотками, пышная зелень – для создания яркого образа.
</p>
<p class="text-justive">
Любое созидание есть отражение философии своего создателя, многогранное искусство, призванное дарить наслаждение. Флориа Гангу  – цветущая река нашего вдохновения, которая  берет начало из времен, когда деревья были немыми…. 
</p>
