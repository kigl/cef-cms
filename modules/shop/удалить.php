<p class="text-justive">
Где найти мудреца, что  скажет, откуда  берет свое начало цветущая река вдохновения, волшебство которой способно исполнять самые заветные желания?  Мы  давно перестали искать ответ на  этот вопрос. Мы взяли себе право творить волшебство!
</p>
<p class="text-justive">
В новом сезоне дизайнеры модного дома АНО представили коллекцию «Floria Gangu  или когда деревья были немыми». Вдохновленная искусством различных эпох, она отражает философию бренда и современный взгляд на одежду. Импровизации на тему викторианского и ретро стилей лишены стереотипов. Женственные рукава, причудливые воротники, мягкие силуэты, наряду со сложными конструктивными решениями моделей создают образ нового сезона.
</p>

